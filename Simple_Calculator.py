# Simple Calculator
num1 = 15
num2 = 6

add = num1 + num2
sub = num1 - num2
multiply = num1 * num2
divide = num1 / num2
print("---SIMPLE CALCULATOR---")
print(f" {num1} + {num2} = {add}")
print(f" {num1} - {num2} = {sub}")
print(f" {num1} * {num2} = {multiply}")
print(f" {num1} / {num2} = {divide}")